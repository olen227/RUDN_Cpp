// Date.h - заголовочный файл для структуры Date
// Структура Date используется для хранения даты (день, месяц, год)

#ifndef DATE_H // Предотвращаем повторное включение
#define DATE_H // Определяем макрос для заголовка

#include <string> // Подключаем string для возврата строкового представления даты

// Структура Date представляет собой простую дату
struct Date {
    int day;   // день
    int month; // месяц
    int year;  // год

    // Конструктор по умолчанию, устанавливает дату в 1 января 1970 года
    Date();
    // Параметризованный конструктор, принимает значения дня, месяца и года
    Date(int day, int month, int year);
    // Метод для получения даты в виде строки в формате DD.MM.YYYY
    std::string toString() const;
};

#endif // Завершение директивы предотвращения двойного включения