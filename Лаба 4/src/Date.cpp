// Date.cpp - файл реализации структуры Date
// Содержит определения конструкторов и метода toString()

#include "Date.h"    // Подключаем собственный заголовочный файл для структуры Date
#include <iomanip>     // Подключаем iomanip для манипуляции вводом/выводом (например, setw и setfill)
#include <sstream>     // Подключаем sstream для использования ostringstream

// Конструктор по умолчанию. Устанавливает дату на 1 января 1970 года
Date::Date() : day(1), month(1), year(1970) {} // инициализируем day=1, month=1, year=1970

// Параметризованный конструктор. Устанавливает поля day, month, year в переданные значения
Date::Date(int day, int month, int year)
    : day(day),     // инициализируем поле day переданным значением day
      month(month), // инициализируем поле month переданным значением month
      year(year) {} // инициализируем поле year переданным значением year

// Метод toString возвращает строковое представление даты в формате DD.MM.YYYY
std::string Date::toString() const {
    std::ostringstream oss;       // Создаём поток для формирования строки
    // Используем setw и setfill для форматирования даты с ведущими нулями
    oss << std::setw(2) << std::setfill('0') << day << "."    // Записываем день с ведущим нулем
        << std::setw(2) << std::setfill('0') << month << "."  // Записываем месяц с ведущим нулем
        << year;                                                // Записываем год
    return oss.str();       // Возвращаем сформированную строку
}